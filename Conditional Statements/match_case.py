# To check whether the number is vowel or consonant
print("Enter an character :")
ch = input()
match ch:
    case 'a':
        print("The character is vowel.")
    case 'A':
        print("The character is vowel.")
    case 'e':
        print("The character is vowel.")
    case 'E':
        print("The character is vowel.")
    case 'i':
        print("The character is vowel.")
    case 'I':
        print("The character is vowel.")
    case 'o':
        print("The character is vowel.")
    case 'O':
        print("The character is vowel.")
    case _:
        print("The character is consonant.")
